const FLEET = new Map();      // normalised plate -> { car, year, color }
const FLEET_BY_NUM = new Map(); // digits-only -> record, or null if ambiguous

for (const line of DATA.trim().split("\n")) {
  const parts = line.split("|");
  const key = (parts[0] || "").trim();
  if (!key) continue;
  const rec = { car: (parts[1] || "").trim(), year: (parts[2] || "").trim(), color: (parts[3] || "").trim() };
  FLEET.set(key, rec);
  const num = key.replace(/^[A-Z]+/, ""); // strip leading plate-code letters
  if (num) {
    if (FLEET_BY_NUM.has(num)) FLEET_BY_NUM.set(num, null); // more than one -> ambiguous
    else FLEET_BY_NUM.set(num, rec);
  }
}

export function normPlate(s) {
  return String(s || "").toUpperCase().replace(/[^A-Z0-9]/g, "");
}

// Look up a car by plate. Tries an exact normalised match first, then falls
// back to the plate number alone (only when that number is unique in the fleet).
export function lookupCar(plate) {
  const k = normPlate(plate);
  if (!k) return null;
  if (FLEET.has(k)) return FLEET.get(k);
  const num = k.replace(/^[A-Z]+/, "");
  if (num && FLEET_BY_NUM.get(num)) return FLEET_BY_NUM.get(num);
  return null;
}

// "CHEVROLET CAPTIVA" -> "Chevrolet Captiva"; keeps model codes (with digits or
// short acronyms like GT / AMG / MG / K5 / GLC) in their original case.
export function carLabel(raw) {
  return String(raw || "")
    .split(/\s+/)
    .map((w) => {
      if (!w) return w;
      if (/\d/.test(w)) return w;          // C300, MG5, GLC300, 350, JS4, T2
      if (w.length <= 3) return w;         // GT, AMG, MG, KIA, JAC, GMC, RAM, GLC
      return w.charAt(0) + w.slice(1).toLowerCase();
    })
    .join(" ")
    .trim();
}

export function colorLabel(raw) {
  const s = String(raw || "").trim();
  if (!s || /\d/.test(s)) return "";       // skip blanks / garbage values
  return s
    .split(/\s+/)
    .map((w) => (w === "&" ? "&" : w.charAt(0) + w.slice(1).toLowerCase()))
    .join(" ");
}
